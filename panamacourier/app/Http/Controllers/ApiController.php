  /**
   * [getPrice description]
   * @param  Request $request [description]
   * @return [type]           [description]
   */
    public function getPrice ( Request $request ) {

      $type = $request->type_package;
      $weight = $request->weight;
      $country = Country::find( $request->country );
      /**
       * [if description]
       * @var [type]
       */
      if ( $weight == 0 || $weight == 0.0 ) {
        $weight = 0.5;
      } else {
        /**
         * [if validamos si el numero es decimal]
         * @var [type]
         */
        if ( strpos($weight, '.') ) {
          $decimal = explode(".", $weight);
          $integer = $decimal[0];
          $left = $decimal[1];
          if ( $left > 5 ) {
            $integer++;
            $weight = round($request->weight, 0, PHP_ROUND_HALF_UP);
          } else if ( $left <= 5 ) {
            $decimal = 0.5;
            $weight = ( $integer + $decimal );
          }
        } else {
          $weight = round($request->weight, 0, PHP_ROUND_HALF_UP);
        }
      }
      $stringweight = strval($weight);
      /**
       * [if Sobre]
       * @var [type]
       */
      if ( $type == '1' ) {
        $dhl_envelope = $this->getPriceAray($type);
        $fedex_envelope = $this->getPriceAray('3');
        if ( $weight > 2.0 ) {
            $price_dhl = null;
        } else {
          if ( $country->dhl_zone ) {
            $price_dhl = $dhl_envelope[ $country->dhl_zone ][ $stringweight ];
          } else {
            $price_dhl = null;
          }
        }
        if ( $country->fedex_zone ) {
          $price_fedex = ( $fedex_envelope[ $country->fedex_zone ][ 'default' ] );
        } else {
          $price_fedex = null;
        }
        return response()->json([
          "price_dhl" => $price_dhl,
          'price_fedex' => $price_fedex
        ]);
      }
      /**
       * [if Paquete]
       * se agrega validacion en peso de fedex
       * se quita validacion de 20kg para dhl
       * @var [type]
       */
      if ( $type == '2' ) {
        if ( ( $country->dhl_zone ) /*&& ( $weight <= 20  )*/ ) {
          $dhl_packages = $this->getPriceAray('2');
          $price_dhl_packages = ( $dhl_packages[ $country->dhl_zone ][ $stringweight ] );
        } else {
          $price_dhl_packages = null;
        }
      if ( ( $country->fedex_zone ) /*&& ( $weight < 2.5 ) */) {
          //$fedex_pak = $this->getPriceAray('4');
          //$price_fedex_pak = ( $fedex_pak[$country->fedex_zone][$weight] );
          $price_fedex_pak = null;
          $fedex_packages = $this->getPriceAray('5');
          $price_fedex_packages = ($fedex_packages[ $country->fedex_zone ][ $stringweight ]);
        } else {
          $price_fedex_pak = null;
          $price_fedex_packages = null;
        }
        /**
         * [return description]
         * @var [type]
         */
        return response()->json([
          'price_dhl_packages' => $price_dhl_packages,
          'price_fedex_pak' => $price_fedex_pak,
          'price_fedex_packages' => $price_fedex_packages
        ]);
      }
    }
